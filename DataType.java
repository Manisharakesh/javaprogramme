package HomeWork;

public class DataType {
    public static void main(String[] args) {
        //PRIMITIVE DATA TYPES
        /*
        BYTE:
        Range:-128 to 127
        memory: 1 byte = 8 bits
         */
        byte b = 25; //define
        System.out.println(b);
        /*

        SHORT:
        Range: -32768 to 32767
        Memory: 2 bytes =16 bits
        */

        short s = 32700;
        System.out.println(s);

        /*
        INTEGER
        Range: QUITE HIGH
        Memory: 4 bytes = 32 bits
        */


        int i= 100000000;
        System.out.println(i);

        /*
        LONG:
        Range: really high
        Memory: 8bytes=64 bits
         */

        long l =10000000000L;
        System.out.println(l);

        //DECIMAL NUMBERS
        /*
        Float:
        Memory: 4 bytes = 32 bits
        Range: -quite high less than double
         */

        float f =12.34f;
        float f1 = (float) 12.22;
        System.out.println(f);

        /*
        DOUBLE:
        Memory: 8 bytes =64bits
        Range -up to 15 decimal places roughly
         */
        double d = 12.44;
        System.out.println(d);
        /*
        CHARACTER (single values - unicode values)
        memory: 2 bytes =16 bits
        */

        char c1 ='A';
        char c2 ='B';
        char c3 ='C';

        System.out.println(c1);

        /*
        BOOLEAN:
        MEMORY: Less than 1bit
        range: true or false
        */
        boolean b1 = true;
        boolean b2 = false;
        System.out.println(b1);


         /*
         STRING: Default class available in java.
         -Any text any length.
          */
        String s1 = "Unify";
        String s2 = "Hello Java";
        String s3 =  "1000";
        String s4 = "1 melton Road";

        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
        System.out.println(s4);








    }
}










