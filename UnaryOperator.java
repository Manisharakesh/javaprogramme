package HomeWork;

public class UnaryOperator {
    public static void main(String[] args){

        int a = 20;
        boolean b= true;
        System.out.println(a++);// return 21
        System.out.println(++a);// return 22
        System.out.println(a--);//return 21
        System.out.println(--a);// return 20
        System.out.println(!b);//return false

    }
}
