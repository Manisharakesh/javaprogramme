package HomeWork;

public class RelationalOperators {
    public static void main(String[] args){


        int a = 70;
        int b=35;
        System.out.println(a == b);//is false
        System.out.println(a != b);// is true
        System.out.println(a > b);// is true
        System.out.println(a < b);//is false
        System.out.println(a >= b);//is true
        System.out.println(a <= b);// is false


    }
}
